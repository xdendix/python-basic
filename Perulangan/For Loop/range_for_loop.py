print(10 * "=", "For Loop default range", 10 * "=")

for i in range(10):
    print(f"Perulangan ke-{i}")

print(10 * "=", "For Loop default range with start and stop", 10 * "=")

for i in range(2, 11):
    print(f"Perulangan ke-{i}")

print(10 * "=", "For Loop default range with start, stop and step", 10 * "=")

for i in range(1, 11, 2):
    print(f"Perulangan ke-{i}")
