print(10 * "#", "Casting String to Int, Float, Boolean", 10 * "#")

# casting string to int
print(type(int("32")))

# casting string to float
print(type(float("30")))

# casting string to boolean
print(type(bool("True")))

print(10 * "#", "Casting Int to String, Float, Boolean", 10 * "#")

# casting int to string
print(type(str(32)))

# casting int to float
print(type(float(32)))

# casting int to boolean
print(type(bool(32)))

print(10 * "#", "Casting Float to String, Int, Boolean", 10 * "#")

# casting float to string
print(type(str(32.2)))

# casting float to int
print(type(int(32.2)))

# casting float to boolean
print(type(bool(32.2)))

print(10 * "#", "Casting Boolean to String, Int, Float", 10 * "#")

# casting boolean to string
print(type(str(True)))

# casting boolean to int
print(type(int(False)))

# casting boolean to float
print(type(float(True)))