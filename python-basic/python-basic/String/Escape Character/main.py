# escape character double quote
text1 = "Ibu kota Indonesia adalah \"Jakarta\""
print(text1)

# escape character single quote
text2 = "Ibu kota Indonesia adalah \'Jakarta\'"
print(text2)

# escape character backslash
text2 = "Ibu kota Indonesia adalah \\Jakarta\\"
print(text2)

# escape character new line
text3 = "Ibu kota Indonesia adalah \nJakarta"
print(text3)

# escape character carriage return (menimpa baris awal)
text4 = "Ibu kota Indonesia adalah \rJakarta"
print(text4)

# escape character tab
text5 = "Ibu kota Indonesia adalah \tJakarta"
print(text5)

# escape character backspace
text6 = "Ibu kota Indonesia adalah \bJakarta"
print(text6)

# escape character octal value
text7 = "Ibu kota Indonesia adalah \111\114\117\126\105\131\117\125Jakarta"
print(text7)
