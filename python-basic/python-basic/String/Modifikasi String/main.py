# upper() huruf besar semua
name = "dendi prayogo hidayat"

print("upper() = ", name.upper())

# lower() huruf kecil semua
name = "DENDI PRAYOGO HIDAYAT"

print("lower() = ", name.lower())

# strip() menghilangkan whitespace
name = "    dendi prayogo hidayat   "

print("strip() = ", name.strip())

# replace() mengganti karakter
name = "dendi prayogo hidayat"

print("replace() = ", name.replace("i", "y"))
