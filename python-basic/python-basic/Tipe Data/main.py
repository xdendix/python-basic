# Tipe Data Integer
a = 1
b = 2
c = a + b
print(f"hasil dari integer 1 + 2 adalah = {c}")

# Tipe Data Float
a = 10.2
b = 11.3
c = a + b
print(f"hasil dari float 10.2 + 11.3 adalah = {c}")

# Tipe Data String
full_name = "Dendi Prayogo Hidayat"
address = "Depok"
email = "dendi@gmail.com"

print(full_name)
print(address)
print(email)

# Tipe Data Boolean
true = True
false = False
print(true)
print(false)

# Tipe Data Type()
print("Hello World:", type("Hello World"))
print("32:", type(32))
print("10.5:", type(10.5))
print("True:", type(True))
print("False:", type(False))