def calc(*args):  # This function takes a variable number of arguments
    return sum(args)  # This returns the sum of all arguments passed to it


result = calc(1, 2, 3, 4, 5)  # The result will be the sum of the numbers 1, 2, 3, 4, and 5

print(f"hasilnya adalah {result}")  # The output will be: hasilnya adalah 15
